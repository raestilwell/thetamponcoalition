				<h3>Our Mission</h3>
				<p><em>The Tampon Coalition strives to deliver free feminine hygiene products to local shelters, schools and businesses in an effort to ease the financial and social burden women endure on a monthly basis.</em></p>
				
				<p>In a society where discussing periods is still stigmatized, access to feminine hygiene products is surprisingly limited-- even in the United States. Often for women who live below the poverty line, their monthly period can bring with it the choice of either eating or staying clean and comfortable. The cost of tampons and pads can often be prohibitive, particularly when items such as food and other basic needs are prioritized. Food stamps and welfare programs do not consider feminine hygiene products to be 'essential items'!</p>
					
				<p>Thanks to your kind donations, the Tampon Coalition is able to put together <a href="blotboxes/">BlotBoxes</a> to deliver throughout the Owensboro Area. Each BlotBox contains enough products to last ten women a month's cycle. For boxes delivered to local schools and businesses, BlotBoxes can last months!</p>
